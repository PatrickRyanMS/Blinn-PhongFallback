public struct ByteVector4
{
    public byte x;
    public byte y;
    public byte z;
    public byte w;

    public ByteVector4(byte x, byte y, byte z, byte w)
    {
        this.x = x;
        this.y = y;
        this.z = z;
        this.w = w;
    }
}

public struct UShortVector4
{
    public ushort x;
    public ushort y;
    public ushort z;
    public ushort w;

    public UShortVector4(ushort x, ushort y, ushort z, ushort w)
    {
        this.x = x;
        this.y = y;
        this.z = z;
        this.w = w;
    }
}
